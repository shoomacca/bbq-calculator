// Helper to determine generalized groups or direct matches
function identifyGroup(cutId: string): string {
  if (bbqRubs[cutId]) return cutId;
  
  if (cutId.includes('jerky')) return 'jerky_default';
  
  const vegCuts = ['bell_peppers', 'eggplant', 'zucchini', 'portobello_mushrooms', 'whole_garlic', 'cauliflower', 'beetroot'];
  if (vegCuts.includes(cutId)) return 'veg_default';

  return 'default';
}

export function getRubsForCut(cutId: string) {
  const group = identifyGroup(cutId);
  return bbqRubs[group] || bbqRubs.default;
}

export function getWoodsForCut(cutId: string) {
  const group = identifyGroup(cutId);
  return bbqWoods[group] || bbqWoods.default;
}
